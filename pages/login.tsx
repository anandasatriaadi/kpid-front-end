import AuthLayout from "@/components/AuthLayout";
import { AuthContext, AuthContextInterface } from "@/context/AuthContext";
import { Button, Divider, Form, Input, message } from "antd";
import Head from "next/head";
import Image from "next/image";
import Link from "next/link";
import { useRouter } from "next/router";
import * as React from "react";
import { NextPageWithLayout } from "./_app";

const Login: NextPageWithLayout = () => {
  const router = useRouter();
  const { isLoggedIn, login } = React.useContext(
    AuthContext
  ) as AuthContextInterface;

  React.useEffect(() => {
    if (isLoggedIn) {
      message.loading("Redirecting...");
      router.push("/");
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [isLoggedIn]);

  const onFinish = (values: any) => {
    login(values);
  };

  const onFinishFailed = (errorInfo: any) => {
    console.error("Failed:", errorInfo);
  };

  return (
    <>
      <Head>
        <title>Login | KPID Jawa Timur</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <section className="flex min-h-screen flex-col bg-slate-100 px-8 md:px-16">
        <div className="my-auto mx-auto max-w-[600px]">
          <div className="mx-auto mb-6 w-1/2 mix-blend-multiply md:w-2/5">
            <Link href={"/"}>
              <div>
                <Image
                  src={"/logo_kpid.png"}
                  width={626}
                  height={160}
                  alt="Logo KPID Jawa Timur"
                />
              </div>
            </Link>
          </div>
          <div className="rounded-lg bg-white p-6 pb-8 shadow-custom">
            <h2 className="text-center text-2xl font-semibold">
              Masuk ke Akun Anda
            </h2>
            <Divider className="my-4" />
            <Form
              name="login_form"
              onFinish={onFinish}
              onFinishFailed={onFinishFailed}
              autoComplete="off"
              layout="vertical"
              requiredMark={"optional"}
            >
              <Form.Item
                label="Email"
                name="email"
                rules={[
                  { required: true, message: "Please input your email!" },
                ]}
              >
                <Input />
              </Form.Item>

              <Form.Item
                label="Password"
                name="password"
                rules={[
                  {
                    required: true,
                    message: "Please input your password!",
                  },
                ]}
              >
                <Input.Password />
              </Form.Item>

              <Form.Item>
                <Button
                  className="mt-4 w-full py-2 text-lg md:text-xl"
                  type="primary"
                  htmlType="submit"
                >
                  Login
                </Button>
              </Form.Item>
            </Form>
          </div>

          <div className="mt-8 rounded-lg bg-slate-200 p-4 text-center text-sm shadow-custom md:text-base">
            Belum memiliki akun?
            <Link href={"/register"}>
              <a className="text-sky-600 hover:text-sky-500"> Daftar</a>
            </Link>
          </div>
        </div>
      </section>
    </>
  );
};

export default Login;

Login.getLayout = function getLayout(page: React.ReactElement) {
  return <AuthLayout>{page}</AuthLayout>;
};
